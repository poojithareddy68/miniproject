package com.infor.controller;

public class DBBook {
	int id;
	String name;
	String Auth;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAuth() {
		return Auth;
	}
	public void setAuth(String Auth) {
		this.Auth = Auth;
	}

}
